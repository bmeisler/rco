recording and touring throughout Europe, Japan, and North and South America. 
Alexander Vereshagin's musical talent and strong leadership have enabled him to 
bring together these uniquely gifted musicians to create an orchestra which has 
been enthusiastically received by audiences and critics alike. They have 
performed at the Russian Center, the Robert Mondavi Festival, various concert 
series in San Francisco, San Jose and Carmel, the Bach Society First 
International Classical Music Fest, the International Music Festival in Yachats, 
Oregon, and a concert tour in Florida. Current plans are to continue a series of 

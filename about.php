premier of Mr. Vereshagin' s <i>Prince Igor Suite, </i>the Russian Chamber 
Orchestra featured some entirely new Vereshagin arrangements of works by 
Shostakovich, Rimsky-Korsakov and others, as well as pieces little known in the 
West. Much of the orchestra's future performances will therefore consist of 
music never or rarely heard before, along with familiar favorites.</p>

